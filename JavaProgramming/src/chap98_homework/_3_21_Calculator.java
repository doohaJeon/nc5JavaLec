package chap98_homework;

public class _3_21_Calculator {
	class Add{
		int add(int a, int b) {
			return a + b;
		}
	}
	class Sub {
		int sub(int a, int b) {
			return a - b;
		}
	}
	class Mul {
		int mul(int a, int b) {
			return a * b;
		}
	}
	class Div {
		int div(int a, int b) {
			return a / b;
		}
	}
}
